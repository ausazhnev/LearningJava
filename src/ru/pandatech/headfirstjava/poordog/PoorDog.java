package ru.pandatech.headfirstjava.poordog;

public class PoorDog {
    private int size;
    private float weight;
    private String name;
    private boolean isDomestic;

    public int getSize() {
        return size;
    }

    public String getName() {
        return name;
    }

    public boolean getDomestic() {
        return isDomestic;
    }

    public float getWeight() {
        return weight;
    }
}
